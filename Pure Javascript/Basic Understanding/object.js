/*------------------------OBJECT------------------------*/
/**Object and Properties */
// Object itu punya property di dalamnya. Properti berisi banyak variabel
// Cara akses properti di dalam dengan titik setelah nama objek!
// objectName.propertyName
// Cara bikin object nih cara pertama!
var myCar = new Object();
myCar.make = "Ford";
myCar.model = "Mustang";
myCar.year = 1969;
// Cara kedua bikin object
var myCar = {
  make: "Ford",
  model: "Mustang",
  year: 1969,
};
// Kalau nggak ada properti di dalamnya keluar undefined. Bukan null!
